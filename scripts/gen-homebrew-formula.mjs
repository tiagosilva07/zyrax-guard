#!/usr/bin/env node
// Generate the Homebrew formula for zyrax-guard from a release checksums.txt.
// Usage: node scripts/gen-homebrew-formula.mjs <version> <checksums.txt> [outFile]
//   <version>  release version WITHOUT leading v, e.g. 0.7.2
//   <checksums.txt>  the release checksums file (sha256<sp><sp>asset per line)
//   [outFile]  write here; otherwise stdout
"use strict";
import { readFileSync, writeFileSync } from "node:fs";

const [, , version, checksumsPath, outFile] = process.argv;
if (!version || !checksumsPath) {
  console.error("usage: node scripts/gen-homebrew-formula.mjs <version> <checksums.txt> [outFile]");
  process.exit(2);
}

const checksums = readFileSync(checksumsPath, "utf8");
function sha(asset) {
  for (const line of checksums.split("\n")) {
    const m = line.trim().match(/^([0-9a-f]{64})\s+(.+)$/);
    if (m && m[2] === asset) return m[1];
  }
  console.error(`checksum not found for ${asset} in ${checksumsPath}`);
  process.exit(1);
}

const base = `https://github.com/tiagosilva07/zyrax-guard/releases/download/v${version}`;
const sums = {
  darwinArm: sha("zyrax-guard-darwin-arm64"),
  darwinAmd: sha("zyrax-guard-darwin-amd64"),
  linuxArm: sha("zyrax-guard-linux-arm64"),
  linuxAmd: sha("zyrax-guard-linux-amd64"),
};

// NOTE: ${...} below are JS-interpolated; #{...} stay literal for Ruby.
const formula = `# typed: false
# frozen_string_literal: true

# Generated by scripts/gen-homebrew-formula.mjs in tiagosilva07/zyrax-guard — do not edit by hand.
class ZyraxGuard < Formula
  desc "Audit AI agent configs (prompt injection, rogue MCP servers) and vet packages"
  homepage "https://github.com/tiagosilva07/zyrax-guard"
  version "${version}"
  license "MIT"

  on_macos do
    on_arm do
      url "${base}/zyrax-guard-darwin-arm64"
      sha256 "${sums.darwinArm}"
    end
    on_intel do
      url "${base}/zyrax-guard-darwin-amd64"
      sha256 "${sums.darwinAmd}"
    end
  end

  on_linux do
    on_arm do
      url "${base}/zyrax-guard-linux-arm64"
      sha256 "${sums.linuxArm}"
    end
    on_intel do
      url "${base}/zyrax-guard-linux-amd64"
      sha256 "${sums.linuxAmd}"
    end
  end

  def install
    bin.install Dir["zyrax-guard-*"].first => "zyrax-guard"
  end

  test do
    assert_match version.to_s, shell_output("#{bin}/zyrax-guard --version")
  end
end
`;

if (outFile) {
  writeFileSync(outFile, formula);
  console.error(`wrote ${outFile}`);
} else {
  process.stdout.write(formula);
}
